-- ============================================================
-- 危险品泄漏应急预案知识库
-- 包含：预案库表、应急任务卡表、初始UN编号预案数据
-- ============================================================

-- ============================================================
-- 1. 应急预案知识库表
-- ============================================================

CREATE TABLE IF NOT EXISTS emergency_plans (
    id                   BIGINT PRIMARY KEY AUTO_INCREMENT,
    plan_no              VARCHAR(32)   NOT NULL UNIQUE COMMENT '预案编号 EP-XXXX',
    un_number            VARCHAR(8)    NOT NULL COMMENT 'UN编号 如1203/1863/1090',
    proper_shipping_name VARCHAR(256)  NOT NULL COMMENT '正确运输名称(中文)',
    proper_shipping_name_en VARCHAR(512) COMMENT '正确运输名称(英文)',
    danger_class         VARCHAR(8)    NOT NULL COMMENT '危险类别: 1爆炸/2气体/3易燃液体/4易燃固体/5氧化剂/6毒性/7放射性/8腐蚀性/9杂类',
    secondary_danger     VARCHAR(32)   COMMENT '次要危险类别',
    packing_group        VARCHAR(4)    COMMENT '包装组: I高/II中/III低',
    hazard_summary       TEXT          NOT NULL COMMENT '危险性概述',
    leak_disposal        TEXT          NOT NULL COMMENT '泄漏处置方法(详细步骤)',
    neutralizer          VARCHAR(512)  COMMENT '中和剂/吸附剂',
    neutralizer_usage    TEXT          COMMENT '中和剂使用方法',
    protective_equipment TEXT          NOT NULL COMMENT '防护装备清单(JSON数组)',
    evacuation_distance  VARCHAR(128)  COMMENT '疏散距离 如:上风300m 下风800m',
    isolation_distance   VARCHAR(128)  COMMENT '隔离距离',
    fire_fighting        TEXT          COMMENT '灭火方法(适用于易燃品)',
    first_aid            TEXT          NOT NULL COMMENT '急救措施',
    environmental_protection TEXT      COMMENT '环境保护措施',
    special_precautions  TEXT          COMMENT '特殊注意事项',
    emergency_contacts   TEXT          COMMENT '应急联系方式(JSON数组)',
    reference_standard   VARCHAR(256)  COMMENT '参考标准 如GB/T 28643/ERG',
    is_builtin           TINYINT       DEFAULT 1 COMMENT '1内置 0自定义',
    status               TINYINT       DEFAULT 1 COMMENT '1启用 0停用',
    created_by           BIGINT,
    created_at           DATETIME      DEFAULT CURRENT_TIMESTAMP,
    updated_at           DATETIME      DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_un (un_number),
    INDEX idx_class (danger_class),
    INDEX idx_status (status),
    INDEX idx_un_class (un_number, danger_class)
) ENGINE=InnoDB COMMENT='危险品泄漏应急预案知识库';

-- ============================================================
-- 2. 应急任务卡表
-- ============================================================

CREATE TABLE IF NOT EXISTS emergency_task_cards (
    id                    BIGINT PRIMARY KEY AUTO_INCREMENT,
    card_no               VARCHAR(32)   NOT NULL UNIQUE COMMENT '任务卡编号 EC-XXXX',
    plan_id               BIGINT        NOT NULL COMMENT '关联预案ID',
    un_number             VARCHAR(8)    NOT NULL COMMENT 'UN编号(冗余)',
    danger_class          VARCHAR(8)    NOT NULL COMMENT '危险类别(冗余)',
    vehicle_id            BIGINT        NOT NULL COMMENT '关联车辆ID',
    driver_id             BIGINT        NOT NULL COMMENT '推送司机ID',
    waybill_id            BIGINT        COMMENT '关联运单ID',
    card_title            VARCHAR(256)  NOT NULL COMMENT '任务卡标题',
    leak_disposal_brief   TEXT          NOT NULL COMMENT '泄漏处置简述(精简版)',
    neutralizer_brief     VARCHAR(512)  COMMENT '中和剂简述',
    protective_equipment_brief TEXT     NOT NULL COMMENT '防护装备简述',
    evacuation_distance   VARCHAR(128)  COMMENT '疏散距离',
    first_aid_brief       TEXT          NOT NULL COMMENT '急救措施简述',
    special_notes         TEXT          COMMENT '特殊注意事项',
    push_channel          VARCHAR(32)   DEFAULT 'app' COMMENT '推送渠道: app/WS/SMS/语音',
    push_status           VARCHAR(16)   DEFAULT 'pending' COMMENT 'pending待推送/pushed已推送/acknowledged已确认/expired已过期',
    pushed_at             DATETIME      COMMENT '推送时间',
    acknowledged_at       DATETIME      COMMENT '司机确认时间',
    source_type           VARCHAR(16)   NOT NULL DEFAULT 'manual' COMMENT 'manual手动/leak_alarm泄漏告警触发/fault_code故障码触发/auto定时检查',
    source_id             BIGINT        COMMENT '触发源ID(如告警ID)',
    status                VARCHAR(16)   DEFAULT 'active' COMMENT 'active有效/completed已完成/cancelled已取消/expired已过期',
    expire_at             DATETIME      COMMENT '过期时间',
    completed_at          DATETIME      COMMENT '完成时间',
    completed_by          BIGINT        COMMENT '完成操作人ID',
    remark                TEXT          COMMENT '备注',
    created_by            BIGINT,
    created_at            DATETIME      DEFAULT CURRENT_TIMESTAMP,
    updated_at            DATETIME      DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_plan (plan_id),
    INDEX idx_vehicle (vehicle_id),
    INDEX idx_driver (driver_id),
    INDEX idx_un (un_number),
    INDEX idx_status (status),
    INDEX idx_push (push_status),
    INDEX idx_created (created_at DESC)
) ENGINE=InnoDB COMMENT='应急任务卡';

